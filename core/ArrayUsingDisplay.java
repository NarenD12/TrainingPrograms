package com.infinite.core;
//Printing all values of a array using display 
public class ArrayUsingDisplay {
	 
	static void Display(){
		int a[]={1,2,3,4,5,6};
		for(int i=0;i<6;i++){
			System.out.print(a[i]+" ");
		}
	}

	public static void main(String[] args) {
	
		Display();
	}
}
