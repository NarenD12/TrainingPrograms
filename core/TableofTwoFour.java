package com.infinite.core;
//Display the Table of 24 
public class TableofTwoFour {

	public static void main(String[] args){
		int i= 1;		//Initialization of i as 1
		int j=24;		//Initialization of j as 24
		while(i<=10){  	//Looping until 10
		System.out.print(j +"x"+ i +"="+j*i+ " ");  //Printing the Table 
		i++;			//Increment of i until 10
		}
	}
}
